"""
模型生成 CoT 的 pointwise 打分 rubric（复用原 rubrics 的三维度定义，只改 framing）。

与原 rubrics.py 的唯一区别：
  - system prompt 从「评 GPT-4 训练标注质量」改为「评 CL 模型推理生成的 CoT 质量」；
  - 评分时把金标答案作为正确性参照（reference answer）。
维度与四档完全沿用论文：logic / grounding(仅含图任务) / knowledge，0–100。
"""
import sys, os
sys.path.insert(0, os.path.dirname(os.path.dirname(os.path.abspath(__file__))))  # 让 import rubrics 生效

import rubrics  # noqa: E402  复用 TASK_DIMENSIONS / detect_subtype
from newapi_client import build_content  # noqa: E402


SYSTEM_PROMPT = (
    "You are a rigorous evaluator of Chain-of-Thought (CoT) reasoning quality. "
    "You will be given a question (optionally with an image), the ground-truth answer, "
    "and a CoT reasoning trace that was GENERATED BY A MODEL under evaluation. "
    "Your job is to judge the QUALITY of that model-generated reasoning along the given "
    "criteria, using the ground-truth answer only as a correctness reference. "
    "Judge the reasoning process itself (not just whether the final answer matches). "
    "Be objective and calibrated. Output ONLY a JSON object, no extra text."
)


def build_messages(task, question, cot_text, image_data_uri=None,
                   ref_answer=None, image_path="", question_id=""):
    """构造一次 pointwise 打分的 messages。返回 (messages, has_grounding)。"""
    dims = rubrics.TASK_DIMENSIONS[task]
    sub = rubrics.detect_subtype(task, image_path, question_id)
    logic = dims["logic"]
    knowledge = dims["knowledge"]
    grounding = dims["grounding"]
    if grounding and "{sub}" in grounding:
        grounding = grounding.format(sub=sub)
    if "{sub}" in knowledge:
        knowledge = knowledge.format(sub=sub)

    crit_lines = [f"- logic: {logic}", f"- knowledge: {knowledge}"]
    if grounding:
        crit_lines.insert(1, f"- grounding: {grounding}")
    crit_block = "\n".join(crit_lines)

    json_fields = '"logic": <0-100>, '
    if grounding:
        json_fields += '"grounding": <0-100>, '
    json_fields += ('"knowledge": <0-100>, "overall": <0-100>, '
                    '"answer_consistent": <true|false>, "rationale": "<one sentence>"')

    ref_line = f"\n### Ground-truth answer (reference):\n{ref_answer}" if ref_answer else ""

    user_text = f"""### Task: judge the quality of the MODEL-GENERATED CoT reasoning below.

Score each criterion from 0 to 100 (0-25 Irrelevant, 26-50 Partially Correct, 51-75 Mostly Correct, 76-100 Fully Correct):
{crit_block}

Also report:
- overall: an overall quality score 0-100.
- answer_consistent: whether the reasoning is internally consistent and correctly leads to the ground-truth answer (true/false).

### Question:
{question}
{ref_line}

### Model-generated CoT reasoning to evaluate:
{cot_text}

### Output (STRICT JSON only):
{{{json_fields}}}"""

    return [
        {"role": "system", "content": SYSTEM_PROMPT},
        {"role": "user", "content": build_content(user_text, image_data_uri)},
    ], bool(grounding)
